    private static java.util.List<WebService.MessageThread> getMessages(WebService.User user) {
        WebService.ChatService_Service service = new WebService.ChatService_Service();
        WebService.ChatService port = service.getChatServicePort();
        return port.getMessages(user);
    }

    private class Update implements Runnable {
        @Override
        public void run() {
            while (true) {
                fullList();
                try {
                    Thread.sleep(4000);
                } catch (InterruptedException ex) {
                    Logger.getLogger(messageThreadPanelGenerator.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }
