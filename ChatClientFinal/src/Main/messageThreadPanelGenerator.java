    private void addElements(DefaultListModel<MessageThread> listModel, ArrayList<MessageThread> thread) {
        for (MessageThread t : thread) {
            if (!listModel.contains(t)) {
                listModel.addElement(t);
            }
        }
        ThreadList.setModel(listModel);
        ThreadList.setCellRenderer(new threadViewGenerator());
    }

    public void fullList() {
        
        ArrayList<MessageThread> thread = (ArrayList) getMessages(Main.BlaBlaClient.currentUser);
        listModel = new DefaultListModel<>();
        addElements(listModel, thread);
    }

