    private void jlblLoginButtonMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlblLoginButtonMouseExited
        //this will change the underlined text type once mouse cursor move away
        Font font = jlblLoginButton.getFont();
        Map attributes = font.getAttributes();
        attributes.put(TextAttribute.UNDERLINE, TextAttribute.UNDERLINE_LOW_GRAY);
        jlblLoginButton.setFont(font.deriveFont(attributes));
    }//GEN-LAST:event_jlblLoginButtonMouseExited

    private void jlblLoginButtonMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlblLoginButtonMouseEntered
        //this will underline the jlabel text onece mouse entered on to the label
        Font font = jlblLoginButton.getFont();
        Map attributes = font.getAttributes();
        attributes.put(TextAttribute.UNDERLINE, TextAttribute.UNDERLINE_ON);
        jlblLoginButton.setFont(font.deriveFont(attributes));
    }//GEN-LAST:event_jlblLoginButtonMouseEntered


