    private static User signUp(java.lang.String username, java.lang.String nickname, java.lang.String password) {
        WebService.UserService_Service service = new WebService.UserService_Service();
        WebService.UserService port = service.getUserServicePort();
        return port.signUp(username, nickname, password);
    }

    
 
