    private static User signIn(java.lang.String username, java.lang.String password) throws UsernameNotFound_Exception, UsernamePasswordMismatch_Exception {
        WebService.UserService_Service service = new WebService.UserService_Service();
        WebService.UserService port = service.getUserServicePort();
        return port.signIn(username, password);
    }

