    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String username = txtUserName.getText();
        String pasword = txtPassword.getText();
        try {
            WebService.User user = signIn(username, pasword);
            if (user != null) {
                BlaBlaClient.currentUser = user;
                BlaBlaClient.changeWindow(new messageThreadPanelGenerator());
            }
        }catch(UsernameNotFound_Exception e){
            lblErrorDisplay.setText("Username not found");
        } catch (UsernamePasswordMismatch_Exception ex) {
            lblErrorDisplay.setText(ex.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

