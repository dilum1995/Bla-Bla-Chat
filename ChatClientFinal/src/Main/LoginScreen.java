    private void jlblSignupButtonMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlblSignupButtonMouseExited
        //this will change the underlined text type once mouse cursor move away
        Font font = jlblSignupButton.getFont();
        Map attributes = font.getAttributes();
        attributes.put(TextAttribute.UNDERLINE, TextAttribute.UNDERLINE_LOW_GRAY);
        jlblSignupButton.setFont(font.deriveFont(attributes));
    }//GEN-LAST:event_jlblSignupButtonMouseExited

    private void jlblSignupButtonMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlblSignupButtonMouseEntered
        //this will underline the jlabel text onece mouse entered on to the label
        Font font = jlblSignupButton.getFont();
        Map attributes = font.getAttributes();
        attributes.put(TextAttribute.UNDERLINE, TextAttribute.UNDERLINE_ON);
        jlblSignupButton.setFont(font.deriveFont(attributes));

    }//GEN-LAST:event_jlblSignupButtonMouseEntered


